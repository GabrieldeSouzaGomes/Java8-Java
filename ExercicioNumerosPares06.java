//5. Contagem de números pares Exiba todos os números pares de 2 a 20 utilizando o laço enquanto.

package lista08;

public class ExercicioNumerosPares06 {

    public static void main(String[] args) {
        int numero = 2;

        while (numero <= 20) {
            System.out.println(numero);
            numero += 2;
        }
    }
}
